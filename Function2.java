public class Function2 {
    public static void main(String[] args) {
        pattern1();
        pattern2();
        pattern3();
    }

    public static void pattern1() {
        System.out.println("*");
        System.out.println("**");
        System.out.println("***");
        System.out.println("****");
        System.out.println("*****");
    }

    public static void pattern2() {
        System.out.println("*****");
        System.out.println("****");
        System.out.println("***");
        System.out.println("**");
        System.out.println("*");
    }

    public static void pattern3() {
        System.out.println("*****");
        System.out.println(" ****");
        System.out.println("  ***");
        System.out.println("   **");
        System.out.println("    *");
    }
}
